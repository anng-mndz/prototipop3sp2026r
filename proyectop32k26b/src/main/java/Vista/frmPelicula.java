       public void cargarTabla() {
        try {
            List<clsPelicula> lista = dao.listar();
            listaPeliculas = lista; // Sincroniza la lista en memoria
            DefaultTableModel modelo = (DefaultTableModel) tblPeliculas.getModel();
            modelo.setRowCount(0);
 
            for (clsPelicula p : lista) {
                Object[] fila = {
                    p.getIdPelicula(),
                    p.getNombre(),
                    p.getClasificacion(),
                    p.getGenero(),
                    p.getSubtitulado(),
                    p.getIdioma(),
                    p.getPrecio()
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar tabla: " + e.getMessage());
        }
    }
        
        public clsPelicula obtenerDatos() {
        clsPelicula p = new clsPelicula();
 
        if (!txtID.getText().isEmpty()) {
            p.setIdPelicula(Integer.parseInt(txtID.getText()));
        }
 
        p.setNombre(txtNombre.getText());
        p.setClasificacion(txtClasificacion.getText());
        p.setGenero(txtGenero.getText());
        p.setSubtitulado(txtSubtitulado.getText());
        p.setIdioma(txtIdioma.getText());
        p.setPrecio(Double.parseDouble(txtPrecio.getText()));
 
        return p;
    }
    
        public void cargarTablaMemoria() {
    DefaultTableModel modelo = (DefaultTableModel) tblPeliculas.getModel();
    modelo.setRowCount(0);

    for (clsPelicula p : listaPeliculas) {
        Object[] fila = {
            p.getIdPelicula(),
            p.getNombre(),
            p.getClasificacion(),
            p.getGenero(),
            p.getSubtitulado(),
            p.getIdioma(),
            p.getPrecio()
        };
        modelo.addRow(fila);
    }
    
}
